if( class_exists('MvcMenuIcons') ) return; 
class MvcMenuIcons {
    function __construct(){
         //change admin menu output
         add_filter( 'wp_edit_nav_menu_walker', array($this, 'customWalkerNavMenuEdit') );
         //add ability to hide icon
         add_filter( 'manage_nav-menus_columns', array( $this, 'addIconToMenuColumns'),99 );  
         //retreive icon data when needed
         add_filter( 'wp_get_nav_menu_items', array( $this, 'addIconToRetrievedData' ) );
         //Save icon data on menu save
         add_action('wp_update_nav_menu_item', array( $this, 'saveIconData'), 3, 3 );
         
         //Add the JQuery for the media uploader
         add_action('admin_head', array( $this, 'jsOutput') );
         
         //Add the icons to outputted menu
         add_filter('wp_nav_menu_objects', array($this,'addIconsToOutput'),5,3 );
    }
    
    
    /**
     * Outputs the icons to the site which have been added
     * 
     * @since 6.21.13
     */
    function addIconsToOutput($items){
        foreach( $items as $item ){
            if( $item->icon != ''){
                
                $format = apply_filters('menu_icons_format', '<img src="%s" />%s', $item );
                
                $img = sprintf($format,$item->icon, $item->title );
                $item->title = $img;
            }
        }
        return $items;
    }
    
    
    /**
     * Outputs the JQuery for the media uploader
     * 
     * @since 4.3.13
     */
    function jsOutput(){
        
        if( get_current_screen()->base != 'nav-menus' ) return;
        
        
        wp_enqueue_script('media-upload');
        wp_enqueue_script('thickbox');
        wp_enqueue_style('thickbox');
        ?>
        <script type="text/javascript">
        jQuery(document).ready(function($) {
             // Upload function goes here
            jQuery('.upload_menu_image_button').click(function() {
                  formField = jQuery(this).attr('rel');
                  tb_show( '', 'media-upload.php?type=image&matcustom=true&TB_iframe=true');
                  return false ;
            });
          
          //This will run when the submit button is pressed
            window.send_to_editor = function(html) {
                if( formField != null){
                  imgurl = jQuery('img',html).attr('src');
                  jQuery('#'+formField).val(imgurl);
                  tb_remove();
                }
            }

            jQuery(document).keyup( function(e) {
                if (e.keyCode == 27) formfield=null;
            });
        });
      </script>
        
        
        <?php   
    }
    
    
    
    /**
     * Save the icons data when menu is saved
     * 
     * @uses filter wp_update_nav_menu_item
     * @return void
     */
    function saveIconData($item, $id){
        if( isset($_POST['menu-item-icon'][$id]) ){
            update_post_meta( $id, '_menu_item_icon', esc_url_raw($_POST['menu-item-icon'][$id]) );
         }
      
    }
    
    
    /**
     * Adds the item to retrieved data about menu items
     * 
     * @uses filter wp_get_nav_menu_items
     * @return obj
     */
    function addIconToRetrievedData($items){
         foreach( $items as $item ){
                $item->icon = get_post_meta($item->db_id,'_menu_item_icon', true);
        }
        return $items;
    }
    
    
    
    
    /**
     * Adds the icon field to the closeable fields in the screen options
     * 
     * @uses filter manage_nav-menus_columns
     * @since 3.26.13
     */
    function addIconToMenuColumns($columns){
        $columns['icon'] = 'Icon';
        return $columns;   
    }
    
    
    /**
     * Change the class used for admin menu output
     * @uses filter wp_edit_nav_menu_walker
     * @uses class found at bottom of this file
     */
    function customWalkerNavMenuEdit(){
      return 'Walker_Nav_Menu_Edit_Custom';
    }
      
}

